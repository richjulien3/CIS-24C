
def sum2(x,y):
    print('The sum of', x,'and',y, 'is: ')
    sumNum = x + y
    return sumNum

def diff2(x,y):
    print('The difference of', x,'and',y, 'is: ')
    diffNum = x - y
    return diffNum

def prod2(x,y):
    print('The product of', x,'and',y, 'is: ')
    prodNum = x * y
    return prodNum

def max2(x,y):
    print('The max of', x,'and',y, 'is: ')
    maxNum = max(x, y)
    return maxNum


